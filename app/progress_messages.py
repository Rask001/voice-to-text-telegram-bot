def get_random_progress_pack() -> ProgressPack:
    if LEGENDARY_PROGRESS_PACKS and random.random() < LEGENDARY_PROGRESS_PROBABILITY:
        return random.choice(LEGENDARY_PROGRESS_PACKS)
    return random.choice(ORDINARY_PROGRESS_PACKS)


def validate_progress_packs(packs: Sequence[Sequence[str]]) -> None:
    for index, pack in enumerate(packs, start=1):
        if len(pack) != 8:
            raise ValueError(f"Progress pack {index} must contain exactly 8 messages")


validate_progress_packs(ORDINARY_PROGRESS_PACKS)
validate_progress_packs(LEGENDARY_PROGRESS_PACKS)
